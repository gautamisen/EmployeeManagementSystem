    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        // TODO add your handling code here:
        String Name = name.getText();
        String Phoneno = phone.getText();
        String Address = address.getText();
        String City = city.getText();
        String State = state.getText();
        String Department = department.getText();
        
        try {
            String validateEmployee = validateEmployee(Name, Phoneno, Address, City, State, Department);
            
            if( validateEmployee.equals("validated")){
                System.out.println("Inside Validated if condition");
                Employee e = new Employee();
                e.setName(Name);
                e.setAddress(Address);
                e.setCity(City);
                e.setPhone(Phoneno);
                e.setState(State);
                e.setDepartment(Department);
                
                String saveEmployee = new EmployeeService().saveEmployee(Config.SESSION, e);
                
                Config.TRANSCATION.commit();
                JOptionPane.showMessageDialog(this, saveEmployee, "Add Employee Form", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                new EmpHomePage().setVisible(true);
                
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_SubmitButtonActionPerformed


