public String validateEmployee(String name,String phone,String address,String city,String state,String department)
{
    if(name.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Name is required", "Add Employee Form", JOptionPane.ERROR_MESSAGE);
        return "notvalidated";
    }
    else if(phone.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Phone number is required" , "Add Employee Form", JOptionPane.ERROR_MESSAGE);
        return "notvalidated";
    }
    else if(address.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Address is required", "Add Employee Form ", JOptionPane.ERROR_MESSAGE);
        return "notvalidated";
    }
    else if(city.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "City is required", "Add employee form", JOptionPane.ERROR_MESSAGE);
        return "notvalidated";
    }
    else if(state.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "State is required", "Add Employee Form", JOptionPane.ERROR_MESSAGE);
        return "notvalidated";
    }
    else if(department.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Department is required", "Add Employee Form",JOptionPane.ERROR_MESSAGE);
        return "notvalidated";
    }
    
    return "validated";
    
}
